/* ---------- Public API -------------------------------------------------- */

/**
 * Scaffold CRUD files for a single entity.
 *
 * @param {object} entity       - { name, pluralName, fields: [...] }
 * @param {string} projectRoot  - absolute path to project root
 * @returns {Promise<{files: string[], css: string}>}
 */
export async function scaffoldEntity(entity, projectRoot) {
  if (!entity || !entity.name || !entity.pluralName || !Array.isArray(entity.fields)) {
    throw new TypeError('entity must have name, pluralName, and fields array');
  }
  if (!projectRoot) {
    throw new TypeError('projectRoot is required');
  }

  const kebab = toKebab(entity.name);
  const files = [];

  // Ensure directories exist
  const dirs = [
    join(projectRoot, 'src', 'server', 'routes'),
    join(projectRoot, 'src', 'public', 'js', 'services'),
    join(projectRoot, 'src', 'public', 'js', 'components'),
  ];
  for (const dir of dirs) {
    await mkdir(dir, { recursive: true });
  }

  // 1. Server route
  const routePath = join(projectRoot, 'src', 'server', 'routes', `${entity.pluralName}.mjs`);
  await writeFile(routePath, generateRouteFile(entity));
  files.push(`src/server/routes/${entity.pluralName}.mjs`);

  // 2. Frontend API service
  const servicePath = join(projectRoot, 'src', 'public', 'js', 'services', `${entity.pluralName}-api.mjs`);
  await writeFile(servicePath, generateServiceFile(entity));
  files.push(`src/public/js/services/${entity.pluralName}-api.mjs`);

  // 3. Card component
  const cardPath = join(projectRoot, 'src', 'public', 'js', 'components', `${kebab}-card.mjs`);
  await writeFile(cardPath, generateCardFile(entity));
  files.push(`src/public/js/components/${kebab}-card.mjs`);

  // 4. Form component
  const formPath = join(projectRoot, 'src', 'public', 'js', 'components', `${kebab}-form.mjs`);
  await writeFile(formPath, generateFormFile(entity));
  files.push(`src/public/js/components/${kebab}-form.mjs`);

  // 5. Detail component
  const detailPath = join(projectRoot, 'src', 'public', 'js', 'components', `${kebab}-detail.mjs`);
  await writeFile(detailPath, generateDetailFile(entity));
  files.push(`src/public/js/components/${kebab}-detail.mjs`);

  // 6. CSS block (returned, not written)
  const css = generateCssBlock(entity);

  return { files, css };
}

/**
 * Scaffold CRUD files for multiple entities and return a server registration snippet.
 *
 * @param {Array<object>} entities   - array of entity definitions
 * @param {string} projectRoot       - absolute path to project root
 * @returns {Promise<{allFiles: string[], cssBlocks: string[], registrationSnippet: string}>}
 */
export async function scaffoldAll(entities, projectRoot) {
  if (!Array.isArray(entities) || entities.length === 0) {
    throw new TypeError('entities must be a non-empty array');
  }

  const allFiles = [];
  const cssBlocks = [];

  for (const entity of entities) {
    const result = await scaffoldEntity(entity, projectRoot);
    allFiles.push(...result.files);
    cssBlocks.push(result.css);
  }

  // Generate registration snippet for src/server/index.mjs
  const imports = entities
    .map(
      (e) =>
        `import { register${e.name}Routes } from './routes/${e.pluralName}.mjs';`,
    )
    .join('\n');

  const mounts = entities
    .map((e) => `register${e.name}Routes(apiRouter);`)
    .join('\n');

  const registrationSnippet = `// --- Auto-generated route registration ---
${imports}

// Mount on your apiRouter (after creating it with Router()):
${mounts}`;

  return { allFiles, cssBlocks, registrationSnippet };
}
